export function modelColor(modelId: string): string {
  let sum = 0
  for (let i = 0; i < modelId.length; i++) {
    sum += modelId.charCodeAt(i)
  }
  const colorIndex = sum % MODEL_COLORS.length
  return MODEL_COLORS[colorIndex]
}
